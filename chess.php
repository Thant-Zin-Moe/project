            <div class="aa zz">The knight (♘, ♞) is a piece in the game of chess, represented by a horse's head and neck. It moves two squares 
                vertically and one square horizontally, or two squares horizontally and one square vertically, jumping over other pieces.</div><br>
        </div> <br>
        
