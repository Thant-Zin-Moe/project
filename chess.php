                <div class="aa">The King(♔, ♚) is a slow piece that can move only one step in every direction-forward, backward, 
                    to the sides or diagonally. The King can capture any of the opponent's pieces that are standing in any square surrounding the King.</div><br>
                
                <div class="aa">The queen (♕, ♛) is the most powerful piece in the game of chess. It can move any number of squares vertically, 
                horizontally or diagonally , combining the powers of the rook and bishop.</div><br>
